package com.example.studentinternshipapp.Model;

public class JobData {
    String title;
    String description;
    String type;


    String id;
    String date;

    public JobData(String title, String description, String type, String id, String date) {
        this.title = title;
        this.description = description;
        this.type = type;
        this.id = id;
        this.date = date;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }
}
